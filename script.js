let countdownTime = 23 * 3600 + 59 * 60 + 59; // Fixed 24 hour shift loop limit logic
const timerInterval = setInterval(() => {
    if (countdownTime <= 0) {
        countdownTime = 24 * 3600; // Reset for dynamic evergreen loop safely
    }
    countdownTime--;

    const h = Math.floor(countdownTime / 3600);
    const m = Math.floor((countdownTime % 3600) / 60);
    const s = countdownTime % 60;

    const elH = document.getElementById('hours');
    const elM = document.getElementById('minutes');
    const elS = document.getElementById('seconds');

    if (elH) elH.textContent = h.toString().padStart(2, '0');
    if (elM) elM.textContent = m.toString().padStart(2, '0');
    if (elS) elS.textContent = s.toString().padStart(2, '0');
}, 1000);

