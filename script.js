function showToast(message, type = 'success') {
    const container = document.getElementById('toast-container');
    if (!container) return;

    const toast = document.createElement('div');
    toast.className = `toast ${type === 'success' ? 'bg-green-600' : 'bg-blue-600'} text-white px-5 py-3 rounded-xl font-bold shadow-2xl transition-all duration-300 transform translate-y-10 opacity-0 text-sm max-w-xs text-center`;
    toast.textContent = message;

    container.appendChild(toast);

    setTimeout(() => {
        toast.classList.remove('translate-y-10', 'opacity-0');
    }, 50);

    setTimeout(() => {
        toast.classList.add('translate-y-10', 'opacity-0');
        setTimeout(() => toast.remove(), 300);
    }, 3000);
}

function copyPromoCode() {
    const code = "WINLANKA";
    const currentMsg = translations[currentLang];
    
    navigator.clipboard.writeText(code).then(() => {
        showToast(currentMsg.toast_copy_success, 'success');
    }).catch(() => {
        // Fallback for older devices/browsers
        const el = document.createElement('textarea');
        el.value = code;
        document.body.appendChild(el);
        el.select();
        try {
            document.execCommand('copy');
            showToast(currentMsg.toast_copy_success, 'success');
        } catch (err) {
            showToast(currentMsg.toast_copy_fail, 'error');
        }
        document.body.removeChild(el);
    });
}

